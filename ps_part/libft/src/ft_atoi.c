int	ft_atoi(const char *str)
{
	int n;
	int neg;
	int i;

	n = 0;
	neg = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\r' || str[i] == '\t' ||
			str[i] == '\v' || str[i] == '\n' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10;
		n = n + (str[i] - 48);
		i++;
	}
	return (n * neg);
}
