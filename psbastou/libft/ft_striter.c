#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int cpt;

	cpt = 0;
	if (s && f)
	{
		while (s[cpt])
		{
			f(&s[cpt]);
			cpt++;
		}
	}
}
